
public class Brick {
	//Location of the brick
	int x;
	int y;
	//Width and height of the brick
	static int w;
	static int h;
	
	
	//Constructors
	public Brick() {
		x = 50;
		y = 50;
		w = 50;
		h = 50;
	}
	public Brick(int locationx, int locationy) {
		x = locationx;
		y = locationy;
		w = 50;
		h = 50;
	}
}
